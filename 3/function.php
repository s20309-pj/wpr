<?php
function addNumbers(int $firstNumber, int $secondNumber): int
{
    return $firstNumber + $secondNumber;
}

function subNumbers(int $firstNumber, int $secondNumber): int
{
    return $firstNumber - $secondNumber;
}

function multiNumbers(int $firstNumber, int $secondNumber): int
{
    return $firstNumber * $secondNumber;
}

function divNumbers(int $firstNumber, int $secondNumber): int
{
    return $firstNumber / $secondNumber;
}

function modNumbers(int $firstNumber, int $secondNumber): int
{
    return $firstNumber % $secondNumber;
}

